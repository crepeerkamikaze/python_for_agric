import streamlit as st

def show():
    st.header("Типы данных в Python")

    st.subheader("Числа")
    st.write("Python поддерживает целочисленные и вещественные числа.")
    st.code("""
    # Целые числа
    x = 5
    y = -10

    # Вещественные числа
    z = 3.14
    w = -2.5
    """, language="python")

    st.subheader("Строки")
    st.write("Строки в Python представляют собой последовательность символов, заключенных в одинарные или двойные кавычки.")
    st.code("""
    # Строки
    name = "Иван"
    message = 'Привет, мир!'
    """, language="python")

    st.subheader("Списки")
    st.write("Списки в Python представляют собой упорядоченные коллекции элементов различных типов.")
    st.code("""
    # Списки
    fruits = ["яблоко", "банан", "апельсин"]
    numbers = [1, 2, 3, 4, 5]
    mixed = ["строка", 42, True, 3.14]
    """, language="python")

    st.subheader("Кортежи")
    st.write("Кортежи в Python представляют собой неизменяемые упорядоченные коллекции элементов различных типов.")
    st.code("""
    # Кортежи
    point = (10.5, 20.7)
    person = ("Иван", 30, "Программист")
    """, language="python")

    st.subheader("Множества")
    st.write("Множества в Python представляют собой неупорядоченные коллекции уникальных элементов.")
    st.code("""
    # Множества
    unique_fruits = set(["яблоко", "банан", "апельсин", "яблоко"])
    numbers = {1, 2, 3, 4, 5, 5}  # Повторяющиеся элементы будут удалены
    """, language="python")

    st.subheader("Словари")
    st.write("Словари в Python представляют собой неупорядоченные коллекции пар ключ-значение.")
    st.code("""
    # Словари
    person = {"name": "Иван", "age": 30, "profession": "Программист"}
    """, language="python")

